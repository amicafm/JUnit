package TDD.calculadora.calculadora;

public class Calculadora {
	//soma
	public int soma (int valorA, int valorB) {
		return valorA + valorB;
	}
	
	//subtrai
	public int subtrai (int valorA, int valorB) {
		return valorA - valorB;
	}
	
	//divide
	public int divide (int valorA, int valorB) {
		return valorA / valorB;
	}

}
